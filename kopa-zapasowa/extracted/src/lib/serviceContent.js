function mapServiceOffer(slug, content, index) {
  return {
    id: `service-offer-${index + 1}`,
    slug,
    title: content.title,
    description: content.description,
    short_description: content.description,
    excerpt: content.description,
    price: Number(content.price || 0),
    currentPrice: Number(content.price || 0),
    type: 'service',
    promoActive: false,
    lowest_price_30_days: null,
    thumbnail_url: content.image ? `/${content.image}` : null,
    noindex: false,
  };
}

export function getServiceOffers() {
  return Object.entries(SERVICE_CONTENT).map(([slug, content], index) => mapServiceOffer(slug, content, index));
}

export function getServiceOfferBySlug(slug) {
  return getServiceOffers().find((service) => service.slug === slug) || null;
}